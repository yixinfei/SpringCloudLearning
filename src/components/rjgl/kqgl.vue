<script>
export default {
  data() {
    return {
      //请求参数
      queryinfo: {
        mc: "",
        pagenum: 1,
        pagesize: 10
      },
      totalpage: 0,
      tableData: [],
      //批量选中data
      selectList: [],
      deleteListId: [],
      zgsj:'08:30:00',
      xlsj:'08:30:00',
      //新增页面
      addFormVisible: false,
      addForm: {
        mc: "",
        xq: ""
      },
      file: "",
      fileList: [],
      progressFlag: "",
      progressPercent: 0
    };
  },
  computed: {},
  mounted() {
    this.getRjxzList();
  },
  methods: {
    //获取数据
    async getRjxzList() {
      //  if (this.$store.getters.getRole("rjgl", "get")) return;
      const res = await this.$http.get("rjgl", {
        params: this.queryinfo
      });
      if (res.meta.status !== 200) {
        return this.$message.error(res.meta.msg);
      }
      this.tableData = res.data.datas;
      this.totalpage = res.data.totalpage;
      this.pagenum = res.data.pagenum;
    },
    //改变显示条数
    handleSizeChange(val) {
      this.queryinfo.pagesize = val;
      this.queryinfo.pagenum = 1;
      this.getRjxzList();
    },
    //页数跳转
    handleCurrentChange(val) {
      this.queryinfo.pagenum = val;
      this.getRjxzList();
    },
    handleSelectionChange(val) {
      this.deleteListId = val.map(item => {
        return item.id;
      });
    },
    //打开添加数据
    handleAdd() {
      //if (this.$store.getters.getRole("roles", "post")) return;

      this.addFormVisible = true;
    },
    addCancel() {
      (this.addFormVisible = false), this.$refs.addForm.resetFields();
    },
    handleExceed(files, fileList) {
      this.$message.warning(
        `当前限制选择 1 个文件，本次选择了 ${
          files.length
        } 个文件，共选择了 ${files.length + fileList.length} 个文件`
      );
    },
    handleChange(file) {
      this.file = file.raw;
    },
    beforeUpload(f) {
      console.log("h1ehe", f);
    },
    //提交数据
    addSubmit() {
      console.log("hehe", this.file);
      this.progressFlag = true;
      this.$http({
        url: "/rjgl",
        method: "post",
        data: { rj: this.file, mc: this.addForm.mc, xq: this.addForm.xq },
        onUploadProgress: progressEvent => {
          // progressEvent.loaded:已上传文件大小
          // progressEvent.total:被上传文件的总大小
          this.progressPercent = parseInt(
            (progressEvent.loaded / progressEvent.total) * 100
          );
        }
      })
        .then(res => {
          console.log(res);
          if (res.meta.status !== 200) {
            this.$message.error(res.meta.msg);
            return;
          }
          this.$message.success(res.meta.msg);
          this.addForm = {};
          this.addFormVisible = false;
          this.file = "";
          this.fileList = [];
          if (this.progressPercent === 100) {
            this.progressFlag = false;
            this.progressPercent = 0;
          }
          this.getRjxzList();
          this.$refs.upload.clearFiles();
        })
        .then(error => {
          console.log(error);
        });
    },
    //删除数据
    handleDeleteList() {
      //  if (this.$store.getters.getRole("roles", "delete")) return;

      if (this.deleteListId.length == 0) {
        this.$message.error("请选择要删除的数据");
        return;
      }
      this.$confirm("确认删除吗？", "提示", {})
        .then(async () => {
          const res = await this.$http.delete("rjgl", {
            data: { ids: this.deleteListId }
          });
          console.log(res);
          if (res.meta.status !== 204) {
            this.$message.error(res.meta.msg);
            return;
          }
          this.$message.success(res.meta.msg);
          this.getRjxzList();
        })
        .catch(() => {});
    },

    //打开修改用户
    handleEdit(index, row) {
      // if (this.$store.getters.getRole("roles", "put")) return;

      this.editForm = JSON.parse(JSON.stringify(row));
      console.log(this.editForm);
      this.editFormVisible = true;
    },
    editCancel() {
      this.editFormVisible = false;
      this.editSubmit = {};
    },
    editSubmit() {
      this.$refs.editForm.validate(async valid => {
        if (!valid) return false;
        const res = await this.$http.put("rjgl", this.editForm);
        if (res.meta.status !== 200) {
          this.$message.error(res.meta.msg);
          return;
        }
        this.$refs.editForm.resetFields();
        this.editFormVisible = false;
        this.getRjxzList();
      });
    },
    editRoleCancel() {
      this.editRoleVisible = false;
    }
  }
};
